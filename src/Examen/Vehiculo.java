package Examen;

public class Vehiculo {
	String modelo;
	String marca;
	String matricula;
	double prestado;
	public Vehiculo(String modelo, String marca, String matricula, double prestado) {
		super();
		this.modelo = modelo;
		this.marca = marca;
		this.matricula = matricula;
		this.prestado = prestado;
	}



	
}
